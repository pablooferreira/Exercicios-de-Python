#Faça um programa que leia um número inteiro qualquer e moestre na tela a sua tabuada.
n = int(input('Digite o valor que quer fazer a tabuáda: '))
zero = n * 0
um = n * 1
dois = n * 2
tres = n * 3
quatro = n * 4
cinco = n * 5
seis = n * 6
sete = n * 7
oito = n * 8
nove = n * 9
dez = n * 10
print('------------')
print('{} x 0 = {}'.format(n, zero))
print('{} x 1 = {}'.format(n, um))
print('{} x 2 = {}'.format(n, dois))
print('{} x 3 = {}'.format(n, tres))
print('{} x 4 = {}'.format(n, quatro))
print('{} x 5 = {}'.format(n, cinco))
print('{} x 6 = {}'.format(n, seis))
print('{} x 7 = {}'.format(n, sete))
print('{} x 8 = {}'.format(n, oito))
print('{} x 9 = {}'.format(n, nove))
print('{} x 10 = {}'.format(n, dez))
print('------------')